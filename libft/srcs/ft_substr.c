#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*str;
	size_t	i;

	i = ft_strlen(s);
	if (i < start || i == 0)
	{
		str = ft_strdup("");
		if (!str)
			return (NULL);
		return (str);
	}
	if (start + len > i)
		len = i - start;
	str = malloc(sizeof(char) * (len + 1));
	if (str == NULL)
		return (NULL);
	i = 0;
	while (i < len)
	{
		str[i] = s[start + i];
		i++;
	}
	str[i] = 0;
	return (str[i] = 0, str);
}
